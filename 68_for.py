#range example
for i in range(10):
    print(i,end=" ")
print("\n---------------------------------")
for i in range(3,10):
    print(i,end=" ")
print("\n---------------------------------")
for i in range(3,10,4):
    print(i,end=" ")
print("\n---------------------------------")
for i in range(30,10,-4):
    print(i,end=" ")
print("\n---------------------------------")

